library(tidyverse)
library(dataReporter)
library(here)
library(haven)

dataset_name <- "hon_2021b"

metadata <- read_csv(here("metadata/metadata.csv")) %>% 
  filter(dataset == dataset_name)

file_type <- tools::file_ext(metadata$filename)

if (file_type == "sav") {
  data <- read_sav(here("data_raw", metadata$dataset, metadata$filename))
} else if (file_type == "dta") {
  data <- read_dta(here("data_raw", metadata$dataset, metadata$filename))
} else {
  stop("Add code for filetype: ", file_type)
}

makeCodebook(data, 
  file = here("data_raw", dataset_name, "codebook_autogenerated.Rmd"), 
  reportTitle = glue::glue("Codebook for {dataset_name}"), output = "html", codebook = TRUE)
